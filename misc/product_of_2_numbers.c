// program to find product of two numbers
// date : 7 oct wed

#include <stdio.h>

int main()
{

    // variable declarations
    int a, b;

    // taking input
    printf("Enter the 2 numbers ");
    scanf("%d%d", &a, &b);

    // logic
    a = a * b;

    // printing output
    printf(" The ans is %d ", a);
    return 0;
}